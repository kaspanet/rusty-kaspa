#[cfg(test)]
mod tests {
    use ark_bn254::Bn254;
    use ark_serialize::CanonicalDeserialize;

    use crate::zk_precompiles::risc0::zk_to_script::groth16::vk::R0_SERIALIZED_UNCOMPRESSED_VK;

    #[test]
    fn verify_upstream_vk_roundtrip() {
        let upstream_vk = risc0_groth16::verifying_key();

        // R0 uses a custom serialization format for the VK which at
        // the end state uses bincode for serialization.
        // See: risc0_groth16::verifier
        // mod serde_ark
        let upstream_serialized_vk = bincode::serialize(&upstream_vk).unwrap();

        // Decode the static VK and ensure it can be deserialized
        ark_groth16::VerifyingKey::<Bn254>::deserialize_uncompressed(R0_SERIALIZED_UNCOMPRESSED_VK.as_slice()).unwrap();

        // Because the end stage of r0 serialization is bincode, funnily enough
        // we have to serialize with bincode in order to prove equivalence.
        assert_eq!(upstream_serialized_vk, bincode::serialize(&R0_SERIALIZED_UNCOMPRESSED_VK.as_slice()).unwrap());
    }
}
